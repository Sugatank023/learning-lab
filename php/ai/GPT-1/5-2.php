<?php

function divide($a,$b){

    if(!is_numeric($a)||is_numeric($b)){
        throw new Exception("数値を入力してください");
    }
    if($b===0){
        throw new Exception("0では割れません");
    }
    return $a/$b;
}

try{
    echo"<p>結果：".divide(10,0)."</p>";
}catch(Exception $e){
    echo"<p>エラー：".$e->getMessage()."</p>";
}
?>

