throw 例外を発生させて処理を中断し、catchブロックに移す
new 新しいインスタンス（オブジェクト）を作る命令
Exception PHPに初めから組み込まれているクラス名
///実際の文//////////////////////////////////////////////////
throw new Exception("0では割れません");
////////////////////////////////////////////////////////////
この地点で処理を強制終了し、"0では割割れません"というエラー情報を持った
Exceptionオブジェクトを投げる

catch (Exception $e)の$eは、throwされたエラー情報（Exceptionオブジェクト）
を受け取る変数

-> とは
PHPにおけるオブジェクトのメンバーアクセス演算子
「オブジェクトが持っている関数やプロパティにアクセスする」ための記号
///実際の文//////////////////////////////////////////////////
$car->start();   // $car の中の start() メソッドを呼び出す
echo $e->getMessage();  //$eというエラー情報から、
                        throwで渡したメッセージを取り出して表示する
